CREATE TABLE IF NOT EXISTS metier.rdv_confirme (
  id_dmd_rdv bigint NOT NULL,
  created_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT rdv_confirme_pkey PRIMARY KEY (id_dmd_rdv),
  CONSTRAINT rdv_confirme_id_dmd_rdv_fkey FOREIGN KEY (id_dmd_rdv) REFERENCES metier.demande_rendez_vous(id_dmd_rdv) ON DELETE CASCADE
