CREATE TABLE IF NOT EXISTS metier.demande_rendez_vous (
  id_dmd_rdv bigint GENERATED BY DEFAULT AS IDENTITY,
  etudiant_id uuid NOT NULL,
  psychologue_id uuid NOT NULL,
  date timestamptz NOT NULL,
  lieu varchar,
  modalite_rdv varchar,
  statut metier.statut_rdv NOT NULL DEFAULT 'en_attente',
