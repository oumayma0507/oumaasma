CREATE TABLE IF NOT EXISTS metier.discussion (
  id_discussion bigint GENERATED BY DEFAULT AS IDENTITY,
  etudiant_id uuid NOT NULL,
  coach_id uuid NOT NULL,
  object_disc varchar,
