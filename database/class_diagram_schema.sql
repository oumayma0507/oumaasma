  url_audio text,
  created_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT question_vocale_pkey PRIMARY KEY (id_question),
  CONSTRAINT question_vocale_id_question_fkey FOREIGN KEY (id_question) REFERENCES metier.question(id_question) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS metier.reponse (
  id_reponse bigint GENERATED BY DEFAULT AS IDENTITY,
  question_id bigint NOT NULL UNIQUE,
  texte_reponse text NOT NULL,
  type varchar NOT NULL CHECK (type IN ('chat', 'vocale')),
