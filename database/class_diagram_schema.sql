BEGIN;

CREATE SCHEMA IF NOT EXISTS metier;

CREATE EXTENSION IF NOT EXISTS pgcrypto;

DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_type t JOIN pg_namespace n ON n.oid = t.typnamespace
    WHERE t.typname = 'role_utilisateur' AND n.nspname = 'metier'
  ) THEN
    CREATE TYPE metier.role_utilisateur AS ENUM ('etudiant', 'coach', 'psychologue', 'admin');
  END IF;
END$$;

DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_type t JOIN pg_namespace n ON n.oid = t.typnamespace
    WHERE t.typname = 'statut_rdv' AND n.nspname = 'metier'
  ) THEN
    CREATE TYPE metier.statut_rdv AS ENUM ('en_attente', 'confirme', 'annule');
  END IF;
END$$;

DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_type t JOIN pg_namespace n ON n.oid = t.typnamespace
    WHERE t.typname = 'type_discussion' AND n.nspname = 'metier'
  ) THEN
    CREATE TYPE metier.type_discussion AS ENUM ('texte', 'audio', 'video');
  END IF;
END$$;

CREATE TABLE IF NOT EXISTS metier.utilisateur (
  id uuid NOT NULL DEFAULT gen_random_uuid(),
  payload_user_id integer UNIQUE,
  nom_prenom varchar NOT NULL,
  login_email varchar NOT NULL UNIQUE,
  avatar text,
  role metier.role_utilisateur NOT NULL,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT utilisateur_pkey PRIMARY KEY (id)
);

CREATE TABLE IF NOT EXISTS metier.admin (
  id uuid NOT NULL,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT admin_pkey PRIMARY KEY (id),
  CONSTRAINT admin_id_fkey FOREIGN KEY (id) REFERENCES metier.utilisateur(id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS metier.etudiant (
  id uuid NOT NULL,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT etudiant_pkey PRIMARY KEY (id),
  CONSTRAINT etudiant_id_fkey FOREIGN KEY (id) REFERENCES metier.utilisateur(id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS metier.coach (
  id uuid NOT NULL,
  is_smart_coach boolean NOT NULL DEFAULT false,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT coach_pkey PRIMARY KEY (id),
  CONSTRAINT coach_id_fkey FOREIGN KEY (id) REFERENCES metier.utilisateur(id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS metier.psychologue (
  id uuid NOT NULL,
  creneau_disponibilite jsonb NOT NULL DEFAULT '[]'::jsonb,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT psychologue_pkey PRIMARY KEY (id),
  CONSTRAINT psychologue_id_fkey FOREIGN KEY (id) REFERENCES metier.utilisateur(id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS metier.bien_etre (
  id_bien_etre bigint GENERATED BY DEFAULT AS IDENTITY,
  etudiant_id uuid NOT NULL UNIQUE,
  etat_critique boolean NOT NULL DEFAULT false,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT bien_etre_pkey PRIMARY KEY (id_bien_etre),
  CONSTRAINT bien_etre_etudiant_id_fkey FOREIGN KEY (etudiant_id) REFERENCES metier.etudiant(id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS metier.detail_bien_etre (
  id_detail bigint GENERATED BY DEFAULT AS IDENTITY,
  bien_etre_id bigint NOT NULL,
  date date NOT NULL DEFAULT CURRENT_DATE,
  humeur varchar,
  sommeil varchar,
  stress varchar,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT detail_bien_etre_pkey PRIMARY KEY (id_detail),
  CONSTRAINT detail_bien_etre_bien_etre_id_fkey FOREIGN KEY (bien_etre_id) REFERENCES metier.bien_etre(id_bien_etre) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS metier.exercice (
  id_exercice bigint GENERATED BY DEFAULT AS IDENTITY,
  description text NOT NULL,
  check_in_etat boolean NOT NULL DEFAULT false,
  type_exercice varchar NOT NULL CHECK (type_exercice IN ('recommande_ia', 'post_seance', 'suivi_quotidien')),
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT exercice_pkey PRIMARY KEY (id_exercice)
);

CREATE TABLE IF NOT EXISTS metier.entretien (
  id_entretien bigint GENERATED BY DEFAULT AS IDENTITY,
  etudiant_id uuid NOT NULL,
  titre varchar NOT NULL,
  date date NOT NULL DEFAULT CURRENT_DATE,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT entretien_pkey PRIMARY KEY (id_entretien),
  CONSTRAINT entretien_etudiant_id_fkey FOREIGN KEY (etudiant_id) REFERENCES metier.etudiant(id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS metier.question (
  id_question bigint GENERATED BY DEFAULT AS IDENTITY,
  entretien_id bigint NOT NULL,
  texte_quest text NOT NULL,
  type varchar NOT NULL CHECK (type IN ('chat', 'vocale')),
  categorie varchar,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT question_pkey PRIMARY KEY (id_question),
  CONSTRAINT question_entretien_id_fkey FOREIGN KEY (entretien_id) REFERENCES metier.entretien(id_entretien) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS metier.question_chat (
  id_question bigint NOT NULL,
  created_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT question_chat_pkey PRIMARY KEY (id_question),
  CONSTRAINT question_chat_id_question_fkey FOREIGN KEY (id_question) REFERENCES metier.question(id_question) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS metier.question_vocale (
