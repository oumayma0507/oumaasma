  created_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT rdv_annule_pkey PRIMARY KEY (id_dmd_rdv),
  CONSTRAINT rdv_annule_id_dmd_rdv_fkey FOREIGN KEY (id_dmd_rdv) REFERENCES metier.demande_rendez_vous(id_dmd_rdv) ON DELETE CASCADE
